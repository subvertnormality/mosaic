local function merge_devices() 
  -- Create a lookup map for faster id-based access
  local stock_map_by_id = {}
  for _, device in ipairs(stock_device_map) do
    stock_map_by_id[device.id] = device
  end

  -- if a stock device doesn't appear in the show device list, remove it
  for index, device in pairs(stock_device_map) do
    if (device.id ~= "none") and (not fn.string_in_table(device_config.show_devices, device.id)) then
      table.remove(stock_device_map, index)
    end
  end

  for _, cd in ipairs(custom_device_map) do
    local sd = stock_map_by_id[cd.id]
    
    -- if sd exists, merge cd into it
    if sd then
      for k, v in pairs(cd) do
        sd[k] = v
      end
      if sd["params"][1].id ~= "none" then
        table.insert(sd["params"], 1, get_none_param())
      end
    end


    -- Handle insertion or removal based on 'hide' attribute
    if not cd.hide then
      if not sd then
        if cd["params"][1].id ~= "none" then
          table.insert(cd.params, 1, get_none_param())
        end
        table.insert(stock_device_map, cd)
        -- Update the lookup map
        stock_map_by_id[cd.id] = cd
      end
    else
      if sd then
        for index, device in ipairs(stock_device_map) do
          if device.id == cd.id then
            table.remove(stock_device_map, index)
            break
          end
        end
        -- Update the lookup map
        stock_map_by_id[cd.id] = nil
      end
    end
  end


  for index, device in pairs(note_players) do
    if string.find(index, "midi", 1, true)  ~= 1 and 
      string.find(index, "jf mpe", 1, true) ~= 1 and 
      string.find(index, "jf kit", 1, true) ~= 1 then
      
      local new_device_params = {}

      table.insert(stock_device_map, { 
        ["type"] = "norns",
        ["name"] = fn.title_case(index),
        ["id"] = index,
        ["unique"] = true,
    
        ["map_params_automatically"] = false,
        ["default_midi_channel"] = nil,
        ["player"] = device,
        ["params"] = new_device_params
      })

      if device.describe().params then
        local device_param_names = device.describe().params
        for i = 1, #device_param_names do

          local param_id = params.lookup[device_param_names[i]]
          local p = params:lookup_param(param_id)

          params:show(param_id)

          local minval = 0
          local maxval = 127
          local quantum = 1

          if p.count then
            minval = 1
            maxval = p.count
          elseif p["controlspec"] then
            quantum = p["controlspec"].quantum or 1
            minval = params:get_range(param_id)[1]
            maxval = params:get_range(param_id)[2]
          end

          local quantum_modifier = 1 / quantum

          table.insert(new_device_params, { 
            ["id"] = device_param_names[i],
            ["name"] = fn.title_case(p.name),
            ["short_descriptor_1"] = fn.format_first_descriptor(p.name),
            ["short_descriptor_2"] = fn.format_second_descriptor(p.name),
            ["cc_min_value"] = minval * quantum_modifier,
            ["cc_max_value"] = maxval * quantum_modifier,
            ["quantum_modifier"] = quantum_modifier,
            ["default"] = p["controlspec"] and p["controlspec"].default or 0
          })
        end
      end

      if device.describe().supports_slew then
        table.insert(new_device_params, { 
          ["id"] = "nb_slew",
          ["name"] = "Slew",
          ["short_descriptor_1"] = "SLEW",
          ["short_descriptor_2"] = "",
          ["cc_min_value"] = -1,
          ["cc_max_value"] = 60,
          ["quantum_modifier"] = 60,
          ["default"] = -1
        })
      end
    end
  end

  -- Update the 'value' field for all devices
  for index, device in ipairs(stock_device_map) do
    device["value"] = index
    
  end

  return stock_device_map
end


local function merge_params(device_params, stock_params)
  local merged_params = {}
  local seen_ids = {} -- hash set for fast id lookup
  
  if (merged_params[1] and merged_params[1].id ~= "none") then
    table.insert(merged_params, get_none_param())

    seen_ids[get_none_param().id] = true
  end

  -- Copy the contents of stock_params into merged_params
  for _, sp in ipairs(stock_params) do
    if not seen_ids[sp.id] then
      table.insert(merged_params, sp)
      seen_ids[sp.id] = true
    end
  end

  -- Add the contents of device_params into merged_params
  for _, dp in ipairs(device_params) do
    if not seen_ids[dp.id] then
      table.insert(merged_params, dp)
      seen_ids[dp.id] = true
    end
  end


  return merged_params
end


