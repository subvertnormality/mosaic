local function create_cc_device() 
  local cc_midi_device = {}

  for i = 1, 127 do
    table.insert(cc_midi_device, {
      ["id"] = "cc_" .. i,
      ["name"] = "CC " .. i,
      ["cc_msb"] = i,
      ["cc_lsb"] = nil,
      ["cc_min_value"] = 0,
      ["cc_max_value"] = 127,
      ["nrpn_msb"] = nil,
      ["nrpn_lsb"] = nil,
      ["nrpn_min_value"] = nil,
      ["nrpn_max_value"] = nil,
      ["short_descriptor_1"] = "CC",
      ["short_descriptor_2"] = i,
    })
  end

  return { 
    ["type"] = "midi",
    ["name"] = "CC Device",
    ["id"] = "cc_device",
    ["map_params_automatically"] = false,
    ["default_midi_channel"] = nil,
    ["params"] = cc_midi_device
  }
end

